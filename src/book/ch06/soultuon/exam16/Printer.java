package book.ch06.soultuon.exam16;

public class Printer {

    void println(int num){
        System.out.println(num);
    }
    void println(boolean a){
        System.out.println(a);
    }
    void println(double num){
        System.out.println(num);
    }
    void println(String a){
        System.out.println(a);
    }
}
